# used to print the specified position or index of the string

name = "ChiranthChiru"
print(name[5])   #prints the 5th index
print(name[0:7]) #prints from index zero to index 6
print(name[:])   #prints from beggining to end
print(name[:10]) #prints from beggining to index 9
print(name[3:])  #prints from specified index to the end

print(name[0::2]) #prints every second string 
print(name[0:10:3]) #prints every third string from index 0 to index 9

print(name[-7])  #starts counting in reverse and prints -7th index
